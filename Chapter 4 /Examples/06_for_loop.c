#include<stdio.h>
    int main(){
    /*
    FOR LOOP
    The syntax of a typical ‘for’ loop looks like this:
        for (initialize; test; increment or decrement)
            {
                //code;
            }
    */ 
    int n = 50;
    for (int i = 10;i<=n ;i++){
        printf("The value of i is %d\n",i);
    }
    printf("");

    return 0;
}